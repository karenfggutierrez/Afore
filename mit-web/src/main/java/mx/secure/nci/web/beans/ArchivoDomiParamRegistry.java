package mx.secure.nci.web.beans;

import java.util.ArrayList;
import java.util.List;

public class ArchivoDomiParamRegistry{
	/**
	 * {@value } Se representa como arreglo para el componente selectBooleanCheckbox
	 * Pero es un solo valor 
	 * */
	private List<String> idBanco;
	private List<String> idsTiposCuenta;
	private Short tipoArchivo;
	private boolean archivoUnico;
	
	{
		this.idBanco = new ArrayList<String>();
		this.idsTiposCuenta= new ArrayList<String>();
		this.archivoUnico=false;
	}
	
	public List<String> getIdBanco() {
		return idBanco;
	}
	public void setIdBanco(List<String> idBanco) {
		this.idBanco = idBanco;
	}
	public List<String> getIdsTiposCuenta() {
		return idsTiposCuenta;
	}
	public void setIdsTiposCuenta(List<String> idsTiposCuenta) {
		this.idsTiposCuenta = idsTiposCuenta;
	}
	public Short getTipoArchivo() {
		return tipoArchivo;
	}
	public void setTipoArchivo(Short tipoArchivo) {
		this.tipoArchivo = tipoArchivo;
	}
	public boolean getArchivoUnico() {
		return archivoUnico;
	}
	public void setArchivoUnico(boolean archivoUnico) {
		this.archivoUnico = archivoUnico;
	}
	
	
}