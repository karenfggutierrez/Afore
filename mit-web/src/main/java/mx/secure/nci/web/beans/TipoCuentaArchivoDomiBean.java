package mx.secure.nci.web.beans;

import java.util.Date;

public class TipoCuentaArchivoDomiBean {
	private GenericCatalogoBean tipoCuenta;
	private ArchivosGeneradosBean archivoCuenta;
	private Short idBanco;
	private Short archivoGenerar;
	private boolean unArchivo;
	private Date fechaInicio;
	private Date fechaFin;
	private String usuario;
	
	
	public Short getIdBanco() {
		return idBanco;
	}
	public void setIdBanco(Short idBanco) {
		this.idBanco = idBanco;
	}
	public Short getArchivoGenerar() {
		return archivoGenerar;
	}
	public void setArchivoGenerar(Short archivoGenerar) {
		this.archivoGenerar = archivoGenerar;
	}
	public boolean isUnArchivo() {
		return unArchivo;
	}
	public void setUnArchivo(boolean unArchivo) {
		this.unArchivo = unArchivo;
	}
	public GenericCatalogoBean getTipoCuenta() {
		return tipoCuenta;
	}
	public void setTipoCuenta(GenericCatalogoBean tipoCuenta) {
		this.tipoCuenta = tipoCuenta;
	}
	public ArchivosGeneradosBean getArchivoCuenta() {
		return archivoCuenta;
	}
	public void setArchivoCuenta(ArchivosGeneradosBean archivoCuenta) {
		this.archivoCuenta = archivoCuenta;
	}
	public Date getFechaInicio() {
		return fechaInicio;
	}
	public void setFechaInicio(Date fechaInicio) {
		this.fechaInicio = fechaInicio;
	}
	public Date getFechaFin() {
		return fechaFin;
	}
	public void setFechaFin(Date fechaFin) {
		this.fechaFin = fechaFin;
	}
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	
}
