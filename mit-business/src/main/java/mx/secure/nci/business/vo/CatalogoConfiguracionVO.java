package mx.secure.nci.business.vo;

import org.apache.commons.lang3.builder.ToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

public class CatalogoConfiguracionVO extends AbstractAuditoriaVO
{
	
	private Short idCatalogoConfiguracion;
	private TipoCatalogoVO tipoCatalogo;
	private String valor;
	private String descripcion;
	private Boolean vigencia;
	
	
	public Short getIdCatalogoConfiguracion() {
		return idCatalogoConfiguracion;
	}
	public void setIdCatalogoConfiguracion(Short idCatalogoConfiguracion) {
		this.idCatalogoConfiguracion = idCatalogoConfiguracion;
	}
	public TipoCatalogoVO getTipoCatalogo() {
		return tipoCatalogo;
	}
	public void setTipoCatalogo(TipoCatalogoVO tipoCatalogo) {
		this.tipoCatalogo = tipoCatalogo;
	}
	public String getValor() {
		return valor;
	}
	public void setValor(String valor) {
		this.valor = valor;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public Boolean getVigencia() {
		return vigencia;
	}
	public void setVigencia(Boolean vigencia) {
		this.vigencia = vigencia;
	}
	

	
	
	@Override
	public String toString() {
		return new ToStringBuilder(this, ToStringStyle.MULTI_LINE_STYLE)
				.append("idCatalogoConfiguracion", idCatalogoConfiguracion)
				.append("tipoCatalogo", tipoCatalogo)
				.append("valor", valor)
				.append("descripcion", descripcion)
				.append("vigencia", vigencia)
				.appendSuper(super.toString())
				.toString();
	}	
	
	

}
