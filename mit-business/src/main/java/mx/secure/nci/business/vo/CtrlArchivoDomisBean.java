package mx.secure.nci.business.vo;

import java.util.List;

public class CtrlArchivoDomisBean {
	
	private String codRet;
	private String msgRet;
	private String desRet;
	private String idArchivo;
	private List<SolicitudVO> domis;
	
	public CtrlArchivoDomisBean() {

	}

	public CtrlArchivoDomisBean(String codRet, String msgRet, String desRet, String idArchivo) {
		this.codRet = codRet;
		this.msgRet = msgRet;
		this.desRet = desRet;
		this.idArchivo = idArchivo;
	}
	
	public CtrlArchivoDomisBean(String codRet, String msgRet, String desRet, String idArchivo,
			List<SolicitudVO> domis) {
		this.codRet = codRet;
		this.msgRet = msgRet;
		this.desRet = desRet;
		this.idArchivo = idArchivo;
		this.domis = domis;
	}

	public List<SolicitudVO> getDomis() {
		return domis;
	}

	public void setDomis(List<SolicitudVO> domis) {
		this.domis = domis;
	}

	public String getCodRet() {
		return codRet;
	}
	public void setCodRet(String codRet) {
		this.codRet = codRet;
	}
	public String getMsgRet() {
		return msgRet;
	}
	public void setMsgRet(String msgRet) {
		this.msgRet = msgRet;
	}
	public String getDesRet() {
		return desRet;
	}
	public void setDesRet(String desRet) {
		this.desRet = desRet;
	}
	public String getIdArchivo() {
		return idArchivo;
	}
	public void setIdArchivo(String idArchivo) {
		this.idArchivo = idArchivo;
	}

	

}
