package mx.secure.nci.business.vo;

public class ValidacionVO 
{
	private Short idValidacion;
	private String tipoValidacion;
	private GenericCatalogoVO estatusValidacion;
	private GenericCatalogoVO rechazoBanco;
	
	public Short getIdValidacion() {
		return idValidacion;
	}
	
	public void setIdValidacion(Short idValidacion) {
		this.idValidacion = idValidacion;
	}
	
	public String getTipoValidacion() {
		return tipoValidacion;
	}
	
	public void setTipoValidacion(String tipoValidacion) {
		this.tipoValidacion = tipoValidacion;
	}
	
	public GenericCatalogoVO getEstatusValidacion() {
		return estatusValidacion;
	}

	public void setEstatusValidacion(GenericCatalogoVO estatusValidacion) {
		this.estatusValidacion = estatusValidacion;
	}
	
	public GenericCatalogoVO getRechazoBanco() {
		return rechazoBanco;
	}
	
	public void setRechazoBanco(GenericCatalogoVO rechazoBanco) {
		this.rechazoBanco = rechazoBanco;
	}	
}