package mx.secure.nci.business.wrapped;

import java.util.HashSet;
import java.util.Set;

public class DomiParameterValuesFilter{
	private Boolean unSoloArchivo;
	private Set<Short> idsBancos;
	private Set<Short> idsTiposCuenta;
	
	{
		idsBancos= new HashSet<Short>();
		idsTiposCuenta= new HashSet<Short>();
	}
	public Set<Short> getIdsBancos() {
		return idsBancos;
	}
	public void setIdsBancos(Set<Short> idsBancos) {
		this.idsBancos = idsBancos;
	}
	public Set<Short> getIdsTiposCuenta() {
		return idsTiposCuenta;
	}
	public void setIdsTiposCuenta(Set<Short> idsTiposCuenta) {
		this.idsTiposCuenta = idsTiposCuenta;
	}
	public Boolean getUnSoloArchivo() {
		return unSoloArchivo;
	}
	public void setUnSoloArchivo(Boolean unSoloArchivo) {
		this.unSoloArchivo = unSoloArchivo;
	}
	
}