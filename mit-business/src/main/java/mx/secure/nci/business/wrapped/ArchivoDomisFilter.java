package mx.secure.nci.business.wrapped;

import java.util.List;

import mx.secure.nci.business.vo.SolicitudVO;

public class ArchivoDomisFilter {
	
	private String claveBanco;
	private String estatus;
	private String fechaInicio;
	private String fechaFin;
	private String nombreArchivo;
	private String usuario;
	private String idArchivo;
	private List<SolicitudVO> domis;

	public ArchivoDomisFilter() {

	}

	public ArchivoDomisFilter(String claveBanco, String estatus, String fechaInicio, String fechaFin,
			String nombreArchivo, String usuario, String idArchivo) {
		this.claveBanco = claveBanco;
		this.estatus = estatus;
		this.fechaInicio = fechaInicio;
		this.fechaFin = fechaFin;
		this.nombreArchivo = nombreArchivo;
		this.usuario = usuario;
		this.idArchivo = idArchivo;
	}
	
	public List<SolicitudVO> getDomis() {
		return domis;
	}

	public void setDomis(List<SolicitudVO> domis) {
		this.domis = domis;
	}

	public String getIdArchivo() {
		return idArchivo;
	}

	public void setIdArchivo(String idArchivo) {
		this.idArchivo = idArchivo;
	}

	public String getClaveBanco() {
		return claveBanco;
	}

	public void setClaveBanco(String claveBanco) {
		this.claveBanco = claveBanco;
	}

	public String getEstatus() {
		return estatus;
	}

	public void setEstatus(String estatus) {
		this.estatus = estatus;
	}

	public String getFechaInicio() {
		return fechaInicio;
	}

	public void setFechaInicio(String fechaInicio) {
		this.fechaInicio = fechaInicio;
	}

	public String getFechaFin() {
		return fechaFin;
	}

	public void setFechaFin(String fechaFin) {
		this.fechaFin = fechaFin;
	}

	public String getNombreArchivo() {
		return nombreArchivo;
	}

	public void setNombreArchivo(String nombreArchivo) {
		this.nombreArchivo = nombreArchivo;
	}

	public String getUsuario() {
		return usuario;
	}

	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	
	

}
