package mx.secure.nci.business.vo;

import java.math.BigDecimal;

public class MatrizConvivenciaVO extends AbstractAuditoriaVO{
	private Long idMarca;
	private String folio;
	private String folioRelacionado;
	private Long numeroCuentaIndividual;
	private GenericCatalogoVO tipoSaldoOpera;
	private GenericCatalogoVO tipoMonto;
	private GenericCatalogoVO movimientoSubcuenta;
	private BigDecimal monto;
	private Byte estatusMarca;

	public Long getIdMarca() {
		return idMarca;
	}

	public void setIdMarca(Long idMarca) {
		this.idMarca = idMarca;
	}

	public Long getNumeroCuentaIndividual() {
		return numeroCuentaIndividual;
	}

	public void setNumeroCuentaIndividual(Long numeroCuentaIndividual) {
		this.numeroCuentaIndividual = numeroCuentaIndividual;
	}

	public GenericCatalogoVO getTipoSaldoOpera() {
		return tipoSaldoOpera;
	}

	public void setTipoSaldoOpera(GenericCatalogoVO tipoSaldoOpera) {
		this.tipoSaldoOpera = tipoSaldoOpera;
	}

	public GenericCatalogoVO getTipoMonto() {
		return tipoMonto;
	}

	public void setTipoMonto(GenericCatalogoVO tipoMonto) {
		this.tipoMonto = tipoMonto;
	}

	public GenericCatalogoVO getMovimientoSubcuenta() {
		return movimientoSubcuenta;
	}

	public void setMovimientoSubcuenta(GenericCatalogoVO movimientoSubcuenta) {
		this.movimientoSubcuenta = movimientoSubcuenta;
	}

	public BigDecimal getMonto() {
		return monto;
	}

	public void setMonto(BigDecimal monto) {
		this.monto = monto;
	}

	public String getFolio() {
		return folio;
	}

	public void setFolio(String folio) {
		this.folio = folio;
	}

	public String getFolioRelacionado() {
		return folioRelacionado;
	}

	public void setFolioRelacionado(String folioRelacionado) {
		this.folioRelacionado = folioRelacionado;
	}

	public Byte getEstatusMarca() {
		return estatusMarca;
	}

	public void setEstatusMarca(Byte estatusMarca) {
		this.estatusMarca = estatusMarca;
	}
}