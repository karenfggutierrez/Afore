package mx.secure.nci.business.vo;

import java.util.Date;

import org.apache.commons.lang3.builder.ToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

public class TipoCatalogoVO {

	private Integer idTipoCat;
	private String nomCat;
	private String desc;
	private Boolean vigencia;
	private Date fehCre;
	private String usuCre;
	private Date fehAct;
	private String usuAct;
	private Integer lineaNegocio;

	public TipoCatalogoVO() {
  }

  public Integer getIdTipoCat() {
    return idTipoCat;
  }

  public TipoCatalogoVO setIdTipoCat(final Integer idTipoCat) {
    this.idTipoCat = idTipoCat;

    return this;
  }

  public String getNomCat() {
    return nomCat;
  }

  public TipoCatalogoVO setNomCat(final String nomCat) {
    this.nomCat = nomCat;

    return this;
  }

  public String getDesc() {
    return desc;
  }

  public TipoCatalogoVO setDesc(final String desc) {
    this.desc = desc;

    return this;
  }

  public Boolean getVigencia() {
    return vigencia;
  }

  public TipoCatalogoVO setVigencia(final Boolean vigencia) {
    this.vigencia = vigencia;

    return this;
  }

  public Date getFehCre() {
    return fehCre;
  }

  public TipoCatalogoVO setFehCre(final Date fehCre) {
    this.fehCre = fehCre;

    return this;
  }

  public String getUsuCre() {
    return usuCre;
  }

  public TipoCatalogoVO setUsuCre(final String usuCre) {
    this.usuCre = usuCre;

    return this;
  }

  public Date getFehAct() {
    return fehAct;
  }

  public TipoCatalogoVO setFehAct(final Date fehAct) {
    this.fehAct = fehAct;

    return this;
  }

  public String getUsuAct() {
    return usuAct;
  }

  public TipoCatalogoVO setUsuAct(final String usuAct) {
    this.usuAct = usuAct;

    return this;
  }

  public Integer getLineaNegocio() {
    return lineaNegocio;
  }

  public TipoCatalogoVO setLineaNegocio(final Integer lineaNegocio) {
    this.lineaNegocio = lineaNegocio;

    return this;
  }

  @Override
  public String toString() {
    return new ToStringBuilder(this, ToStringStyle.DEFAULT_STYLE).appendSuper(super.toString())
        .append("idTipoCat", idTipoCat).append("nomCat", nomCat).append("desc", desc).append("vigencia", vigencia)
        .append("fehCre", fehCre).append("usuCre", usuCre).append("fehAct", fehAct).append("usuAct", usuAct)
        .append("lineaNegocio", lineaNegocio).toString();
  }


}
