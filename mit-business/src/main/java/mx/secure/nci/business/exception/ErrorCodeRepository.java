package mx.secure.nci.business.exception;

public enum ErrorCodeRepository 
{
	// EXCEPTION RANGO DE 1100 A 1199
	
	EX_PERSISTENCE_EXCEPTION("1100"),
	/*
	 * SE AGREGAN CODIGOS DE  EXCEPTION. EL MAXIMO DE EXCEPTION SON 1199
	 */
	EX_EXCEPTION("1199");
	
	
	// ERRORES CONTROLADOS RANGO DE 1200 A 1299
	/*
	 * SE AGREGAN CODIGOS DE ERRORRES. EL MAXIMO DE ERRORES SON 1299
	 */
	
	
	private String errorCode;
	
	ErrorCodeRepository(String errorCode)
	{
		this.errorCode=errorCode;
	}
	
	public String getErrorCode()
	{
		return this.errorCode;
	}	

}
