package mx.secure.nci.business.report.beans;

import java.math.BigDecimal;
import java.util.Date;

public class CargosDataReportBean {
	
	private Long numCuentaIndividual;
	private BigDecimal importe;
	private Date fechaEstatus;
	private String estatusCargo;
	private String rechazoBanco;
	private String periodo;
	
	public Long getNumCuentaIndividual() {
		return numCuentaIndividual;
	}
	
	public void setNumCuentaIndividual(Long numCuentaIndividual) {
		this.numCuentaIndividual = numCuentaIndividual;
	}
	
	public BigDecimal getImporte() {
		return importe;
	}
	
	public void setImporte(BigDecimal importe) {
		this.importe = importe;
	}
	
	public Date getFechaEstatus() {
		return fechaEstatus;
	}
	
	public void setFechaEstatus(Date fechaEstatus) {
		this.fechaEstatus = fechaEstatus;
	}
	
	public String getEstatusCargo() {
		return estatusCargo;
	}
	
	public void setEstatusCargo(String estatusCargo) {
		this.estatusCargo = estatusCargo;
	}
	
	public String getRechazoBanco() {
		return rechazoBanco;
	}
	
	public void setRechazoBanco(String rechazoBanco) {
		this.rechazoBanco = rechazoBanco;
	}
	
	public String getPeriodo() {
		return periodo;
	}
	
	public void setPeriodo(String periodo) {
		this.periodo = periodo;
	}	
}