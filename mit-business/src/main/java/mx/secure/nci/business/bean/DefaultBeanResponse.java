package mx.secure.nci.business.bean;

import java.io.Serializable;

public class DefaultBeanResponse implements Serializable {
	private static final long serialVersionUID = 1L;

	protected String codRet;
	protected String msgRet;
	protected String desRet;

	public DefaultBeanResponse() {
		super();
	}

	public DefaultBeanResponse(String codRet, String msgRet, String desRet) {
		super();
		this.codRet = codRet;
		this.msgRet = msgRet;
		this.desRet = desRet;
	}

	public String getCodRet() {
		return codRet;
	}

	public void setCodRet(String codRet) {
		this.codRet = codRet;
	}

	public String getMsgRet() {
		return msgRet;
	}

	public void setMsgRet(String msgRet) {
		this.msgRet = msgRet;
	}

	public String getDesRet() {
		return desRet;
	}

	public void setDesRet(String desRet) {
		this.desRet = desRet;
	}
}
