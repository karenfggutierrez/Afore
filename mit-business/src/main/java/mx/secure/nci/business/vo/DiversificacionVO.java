package mx.secure.nci.business.vo;

import java.math.BigDecimal;

import org.apache.commons.lang3.builder.ToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

public class DiversificacionVO extends AbstractAuditoriaVO 
{
	private String claveSolicitud;
	private String folio;
	private GenericCatalogoVO fondoAportacionVoluntaria;
	private BigDecimal monto;
	private Short porcentaje;
	
	private Short idFondo;
	private String valor;
	private Integer rowId;
	private	Integer	idArchivo;
	private	String	idCargoStr;
	private	Integer	idPrioridad;
	private	String	usuarioCreacion;
	private Long idArchivoDomi;
	
	public Long getIdArchivoDomi() {
		return idArchivoDomi;
	}

	public void setIdArchivoDomi(Long idArchivoDomi) {
		this.idArchivoDomi = idArchivoDomi;
	}

	public String getUsuarioCreacion() {
		return usuarioCreacion;
	}

	public void setUsuarioCreacion(String usuarioCreacion) {
		this.usuarioCreacion = usuarioCreacion;
	}

	public Integer getIdPrioridad() {
		return idPrioridad;
	}

	public void setIdPrioridad(Integer idPrioridad) {
		this.idPrioridad = idPrioridad;
	}


	public String getIdCargoStr() {
		return idCargoStr;
	}

	public void setIdCargoStr(String idCargoStr) {
		this.idCargoStr = idCargoStr;
	}

	public Integer getIdArchivo() {
		return idArchivo;
	}

	public void setIdArchivo(Integer idArchivo) {
		this.idArchivo = idArchivo;
	}

	public String getFolio() {
		return folio;
	}

	public void setFolio(String folio) {
		this.folio = folio;
	}

	public GenericCatalogoVO getFondoAportacionVoluntaria() {
		return fondoAportacionVoluntaria;
	}

	public void setFondoAportacionVoluntaria(GenericCatalogoVO fondoAportacionVoluntaria) {
		this.fondoAportacionVoluntaria = fondoAportacionVoluntaria;
	}

	public BigDecimal getMonto() {
		return monto;
	}

	public void setMonto(BigDecimal monto) {
		this.monto = monto;
	}

	public Short getPorcentaje() {
		return porcentaje;
	}

	public void setPorcentaje(Short porcentaje) {
		this.porcentaje = porcentaje;
	}

	public String getClaveSolicitud() {
		return claveSolicitud;
	}

	public void setClaveSolicitud(String claveSolicitud) {
		this.claveSolicitud = claveSolicitud;
	}

	@Override
	public String toString() {
		return new ToStringBuilder(this, ToStringStyle.MULTI_LINE_STYLE)				
				.append("claveSolicitud", claveSolicitud)
				.append("folio", folio)
				.append("fondoAportacionVoluntaria", fondoAportacionVoluntaria)
				.append("monto", monto).append("porcentaje", porcentaje)
				.appendSuper(super.toString())
				.toString();
	}

	public Short getIdFondo() {
		return idFondo;
	}

	public void setIdFondo(Short idFondo) {
		this.idFondo = idFondo;
	}

	public String getValor() {
		return valor;
	}

	public void setValor(String valor) {
		this.valor = valor;
	}

	public Integer getRowId() {
		return rowId;
	}

	public void setRowId(Integer rowId) {
		this.rowId = rowId;
	}


}
