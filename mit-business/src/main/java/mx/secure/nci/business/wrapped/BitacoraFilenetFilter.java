package mx.secure.nci.business.wrapped;

public class BitacoraFilenetFilter {
	private String idBitacora;
	private String folio;
	private String folioTramite;
	private String reintentos;
	private String idEtapa;
	private String idEstatus;
	private String desc;
	private String usuario;
	
	public BitacoraFilenetFilter () {
		
	}
	
	public BitacoraFilenetFilter ( String idBitacora
								 , String folio
								 , String folioTramite
								 , String reintentos
								 , String idEtapa
								 , String idEstatus
								 , String desc
								 , String usuario) {
		this.idBitacora = idBitacora;
		this.folio = folio;
		this.folioTramite = folioTramite;
		this.reintentos = reintentos;
		this.idEtapa = idEtapa;
		this.idEstatus = idEstatus;
		this.desc = desc;
		this.usuario = usuario;
	}

	public String getIdBitacora() {
		return idBitacora;
	}

	public void setIdBitacora(String idBitacora) {
		this.idBitacora = idBitacora;
	}

	public String getFolio() {
		return folio;
	}

	public void setFolio(String folio) {
		this.folio = folio;
	}

	public String getFolioTramite() {
		return folioTramite;
	}

	public void setFolioTramite(String folioTramite) {
		this.folioTramite = folioTramite;
	}

	public String getReintentos() {
		return reintentos;
	}

	public void setReintentos(String reintentos) {
		this.reintentos = reintentos;
	}

	public String getIdEtapa() {
		return idEtapa;
	}

	public void setIdEtapa(String idEtapa) {
		this.idEtapa = idEtapa;
	}

	public String getIdEstatus() {
		return idEstatus;
	}

	public void setIdEstatus(String idEstatus) {
		this.idEstatus = idEstatus;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	public String getUsuario() {
		return usuario;
	}

	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	
	

}
