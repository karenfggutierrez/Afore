package mx.secure.nci.business.bean;

public class RespGeneracionArchivosDomi {
	public String idArchivo; 
	public String nombreArchivo; 
	
	public RespGeneracionArchivosDomi  () {
		
	}
	
	public RespGeneracionArchivosDomi  (String idArchivo, String nombreArchivo) {
		this.idArchivo = idArchivo;
		this.nombreArchivo = nombreArchivo;
	}

	public String getIdArchivo() {
		return idArchivo;
	}

	public void setIdArchivo(String idArchivo) {
		this.idArchivo = idArchivo;
	}

	public String getNombreArchivo() {
		return nombreArchivo;
	}

	public void setNombreArchivo(String nombreArchivo) {
		this.nombreArchivo = nombreArchivo;
	}
	
	
}
