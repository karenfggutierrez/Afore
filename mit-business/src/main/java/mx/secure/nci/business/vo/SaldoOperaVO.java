package mx.secure.nci.business.vo;

import java.io.Serializable;

public class SaldoOperaVO implements Serializable
{
	private Short idSaldoOpera;
	private String descripcion;
	

	
	public SaldoOperaVO(){}



	public Short getIdSaldoOpera() {
		return idSaldoOpera;
	}



	public void setIdSaldoOpera(Short idSaldoOpera) {
		this.idSaldoOpera = idSaldoOpera;
	}



	public String getDescripcion() {
		return descripcion;
	}



	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}



	@Override
	public String toString() {
		return "SaldoOperaVO "+ "["+ "idSaldoOpera = " + idSaldoOpera + ", descripcion = "+ descripcion + "]";
	}
	
	 
	
	
	
	

}
