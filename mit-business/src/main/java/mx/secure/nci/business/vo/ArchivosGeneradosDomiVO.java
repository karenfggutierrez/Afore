package mx.secure.nci.business.vo;

public class ArchivosGeneradosDomiVO {
	private String fechaIniGen;
	private String fechaFinGen;
	private String tipoArchivo;
	private String tipoArchivoDesc;
	private String tipoCta;
	private String tipoCtaDesc;
	private String cveBanco;
	private String cveBancoDesc;
	private boolean estatus;
	
	public ArchivosGeneradosDomiVO () {
		
	}
	
	public ArchivosGeneradosDomiVO ( String fechaIniGen
								   , String fechaFinGen
								   , String tipoArchivo
								   , String tipoArchivoDesc
								   , String tipoCta
								   , String tipoCtaDesc
								   , String cveBanco
								   , String cveBancoDesc
								   , boolean estatus) {
		this.fechaIniGen = fechaIniGen;
		this.fechaFinGen = fechaFinGen;
		this.tipoArchivo = tipoArchivo;
		this.tipoArchivoDesc = tipoArchivoDesc;
		this.tipoCta = tipoCta;
		this.tipoCtaDesc = tipoCtaDesc;
		this.cveBanco = cveBanco;
		this.cveBancoDesc = cveBancoDesc;
		this.estatus = estatus;
	}

	public String getFechaIniGen() {
		return fechaIniGen;
	}

	public void setFechaIniGen(String fechaIniGen) {
		this.fechaIniGen = fechaIniGen;
	}

	public String getFechaFinGen() {
		return fechaFinGen;
	}

	public void setFechaFinGen(String fechaFinGen) {
		this.fechaFinGen = fechaFinGen;
	}

	public String getTipoArchivo() {
		return tipoArchivo;
	}

	public void setTipoArchivo(String tipoArchivo) {
		this.tipoArchivo = tipoArchivo;
	}

	public String getTipoArchivoDesc() {
		return tipoArchivoDesc;
	}

	public void setTipoArchivoDesc(String tipoArchivoDesc) {
		this.tipoArchivoDesc = tipoArchivoDesc;
	}

	public String getTipoCta() {
		return tipoCta;
	}

	public void setTipoCta(String tipoCta) {
		this.tipoCta = tipoCta;
	}

	public String getTipoCtaDesc() {
		return tipoCtaDesc;
	}

	public void setTipoCtaDesc(String tipoCtaDesc) {
		this.tipoCtaDesc = tipoCtaDesc;
	}

	public String getCveBanco() {
		return cveBanco;
	}

	public void setCveBanco(String cveBanco) {
		this.cveBanco = cveBanco;
	}

	public String getCveBancoDesc() {
		return cveBancoDesc;
	}

	public void setCveBancoDesc(String cveBancoDesc) {
		this.cveBancoDesc = cveBancoDesc;
	}

	public boolean isEstatus() {
		return estatus;
	}

	public void setEstatus(boolean estatus) {
		this.estatus = estatus;
	}

}
