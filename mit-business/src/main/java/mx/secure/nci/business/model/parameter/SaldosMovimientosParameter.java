package mx.secure.nci.business.model.parameter;

import java.io.Serializable;
import java.util.Date;
import java.util.List;

import org.apache.commons.lang3.builder.ToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

public class SaldosMovimientosParameter implements Serializable
{

	/**
	 *
	 */
	private static final long serialVersionUID = 1L;



	private Date fechaIncio;
	private Date fechaFin;
	private Long numCtaInvdual;
	private List<Short> idSiefores;

	private List<String> listTablas;

	private List<Short> idTipoSubctasMovAvol;
	private List<Short> idTipoSubctasMovComp;
	private List<Short> idTipoSubctasMovGob;
	private List<Short> idTipoSubctasMovRcv;
	private List<Short> idTipoSubctasMovSar;
	private List<Short> idTipoSubctasMovViv;


	public Date getFechaIncio() {
		return fechaIncio;
	}
	public void setFechaIncio(final Date fechaIncio) {
		this.fechaIncio = fechaIncio;
	}
	public Date getFechaFin() {
		return fechaFin;
	}
	public void setFechaFin(final Date fechaFin) {
		this.fechaFin = fechaFin;
	}
	public Long getNumCtaInvdual() {
		return numCtaInvdual;
	}
	public void setNumCtaInvdual(final Long numCtaInvdual) {
		this.numCtaInvdual = numCtaInvdual;
	}
	public List<Short> getIdSiefores() {
		return idSiefores;
	}
	public void setIdSiefores(final List<Short> idSiefores) {
		this.idSiefores = idSiefores;
	}
	public List<String> getListTablas() {
		return listTablas;
	}
	public void setListTablas(final List<String> listTablas) {
		this.listTablas = listTablas;
	}
	public List<Short> getIdTipoSubctasMovAvol() {
		return idTipoSubctasMovAvol;
	}
	public void setIdTipoSubctasMovAvol(final List<Short> idTipoSubctasMovAvol) {
		this.idTipoSubctasMovAvol = idTipoSubctasMovAvol;
	}
	public List<Short> getIdTipoSubctasMovComp() {
		return idTipoSubctasMovComp;
	}
	public void setIdTipoSubctasMovComp(final List<Short> idTipoSubctasMovComp) {
		this.idTipoSubctasMovComp = idTipoSubctasMovComp;
	}
	public List<Short> getIdTipoSubctasMovGob() {
		return idTipoSubctasMovGob;
	}
	public void setIdTipoSubctasMovGob(final List<Short> idTipoSubctasMovGob) {
		this.idTipoSubctasMovGob = idTipoSubctasMovGob;
	}
	public List<Short> getIdTipoSubctasMovRcv() {
		return idTipoSubctasMovRcv;
	}
	public void setIdTipoSubctasMovRcv(final List<Short> idTipoSubctasMovRcv) {
		this.idTipoSubctasMovRcv = idTipoSubctasMovRcv;
	}
	public List<Short> getIdTipoSubctasMovSar() {
		return idTipoSubctasMovSar;
	}
	public void setIdTipoSubctasMovSar(final List<Short> idTipoSubctasMovSar) {
		this.idTipoSubctasMovSar = idTipoSubctasMovSar;
	}
	public List<Short> getIdTipoSubctasMovViv() {
		return idTipoSubctasMovViv;
	}
	public void setIdTipoSubctasMovViv(final List<Short> idTipoSubctasMovViv) {
		this.idTipoSubctasMovViv = idTipoSubctasMovViv;
	}


	@Override
	public String toString() {
		return new ToStringBuilder(this, ToStringStyle.DEFAULT_STYLE)
				.appendSuper(super.toString()).append("fechaIncio", fechaIncio)
				.append("fechaFin", fechaFin)
				.append("numCtaInvdual", numCtaInvdual)
				.append("idSiefores", idSiefores)
				.append("listTablas", listTablas)
				.append("idTipoSubctasMovAvol", idTipoSubctasMovAvol)
				.append("idTipoSubctasMovComp", idTipoSubctasMovComp)
				.append("idTipoSubctasMovGob", idTipoSubctasMovGob)
				.append("idTipoSubctasMovRcv", idTipoSubctasMovRcv)
				.append("idTipoSubctasMovSar", idTipoSubctasMovSar)
				.append("idTipoSubctasMovViv", idTipoSubctasMovViv).toString();
	}



}
