package mx.secure.nci.business.wrapped;

import java.util.List;
import mx.secure.nci.business.vo.PeticionDomiciliacionVO;

public class CifrasTotalesDomiFilter {
	private String origenAportacion;
	private String fechaInicio;
	private String fechaFin;
	private List <Integer> tipoCta;
	private List <Integer> cveBanco;
	
	public CifrasTotalesDomiFilter () {
		
	}
	
	public CifrasTotalesDomiFilter ( String origenAportacion
						    	   , String fechaInicio
						    	   , String fechaFin
						    	   , List <Integer> tipoCta
						    	   , List <Integer> cveBanco) {
		this.origenAportacion = origenAportacion;
		this.fechaInicio = fechaInicio;
		this.fechaFin = fechaFin;
		this.tipoCta = tipoCta;
		this.cveBanco = cveBanco;
	}

	public String getOrigenAportacion() {
		return origenAportacion;
	}

	public void setOrigenAportacion(String origenAportacion) {
		this.origenAportacion = origenAportacion;
	}

	public String getFechaInicio() {
		return fechaInicio;
	}

	public void setFechaInicio(String fechaInicio) {
		this.fechaInicio = fechaInicio;
	}

	public String getFechaFin() {
		return fechaFin;
	}

	public void setFechaFin(String fechaFin) {
		this.fechaFin = fechaFin;
	}

	public List<Integer> getTipoCta() {
		return tipoCta;
	}

	public void setTipoCta(List<Integer> tipoCta) {
		this.tipoCta = tipoCta;
	}

	public List<Integer> getCveBanco() {
		return cveBanco;
	}

	public void setCveBanco(List<Integer> cveBanco) {
		this.cveBanco = cveBanco;
	}

	
}
