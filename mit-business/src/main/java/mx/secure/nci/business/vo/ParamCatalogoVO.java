package mx.secure.nci.business.vo;

public class ParamCatalogoVO {

  private Short idLineaNegocio;
  private Short idTipoCatalogo;
  private Short idCatalogoPadre;
  private Short idCatalogo;

  
  public ParamCatalogoVO() {
    super();
  }
  
  
  public ParamCatalogoVO(Short idTipoCatalogo) 
  {
	  this.idTipoCatalogo = idTipoCatalogo;
  }
  

  public ParamCatalogoVO(Short idLineaNegocio, 
		  				 Short idTipoCatalogo,
		  				 Short idCatalogoPadre, 
		  				 Short idCatalogo) 
  {

	this.idLineaNegocio = idLineaNegocio;
	this.idTipoCatalogo = idTipoCatalogo;
	this.idCatalogoPadre = idCatalogoPadre;
	this.idCatalogo = idCatalogo;
}





public Short getIdLineaNegocio() {
    return idLineaNegocio;
  }

  public ParamCatalogoVO setIdLineaNegocio(final Short idLineaNegocio) {
    this.idLineaNegocio = idLineaNegocio;

    return this;
  }

  public Short getIdTipoCatalogo() {
    return idTipoCatalogo;
  }

  public ParamCatalogoVO setIdTipoCatalogo(final Short idTipoCatalogo) {
    this.idTipoCatalogo = idTipoCatalogo;

    return this;
  }

  public Short getIdCatalogoPadre() {
    return idCatalogoPadre;
  }

  public ParamCatalogoVO setIdCatalogoPadre(final Short idCatalogoPadre) {
    this.idCatalogoPadre = idCatalogoPadre;

    return this;
  }

  public Short getIdCatalogo() {
    return idCatalogo;
  }

  public ParamCatalogoVO setIdCatalogo(final Short idCatalogo) {
    this.idCatalogo = idCatalogo;

    return this;
  }

}
