package mx.secure.nci.business.vo;

import java.math.BigDecimal;

public class ReporteCifrasControlAbonoVO {
	
	private BigDecimal importeAbono;
	private String fondoEntrada;
	
	private Integer porcentaje;
	private String porcentajeString;
	
	
	
	public Integer getPorcentaje() {
		return porcentaje;
	}
	public void setPorcentaje(Integer porcentaje) {
		this.porcentaje = porcentaje;
	}
	public String getPorcentajeString() {
		return porcentajeString;
	}
	public void setPorcentajeString(String porcentajeString) {
		this.porcentajeString = porcentajeString;
	}
	public BigDecimal getImporteAbono() {
		return importeAbono;
	}
	public void setImporteAbono(BigDecimal importeAbono) {
		this.importeAbono = importeAbono;
	}
	public String getFondoEntrada() {
		return fondoEntrada;
	}
	public void setFondoEntrada(String fondoEntrada) {
		this.fondoEntrada = fondoEntrada;
	}
	
}
