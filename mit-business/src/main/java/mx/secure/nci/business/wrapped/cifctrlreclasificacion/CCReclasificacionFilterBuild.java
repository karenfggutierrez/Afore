package mx.secure.nci.business.wrapped.cifctrlreclasificacion;

public class CCReclasificacionFilterBuild {
	
	private CifCtrlReclasificacionFilter filter;

	public CCReclasificacionFilterBuild() {
		super();
		this.filter = new CifCtrlReclasificacionFilter();
	}
	
    public CifCtrlReclasificacionFilter build() {
        return filter;
    }

	public CCReclasificacionFilterBuild setFolioCifCtrl(String folioCifCtrl) {
		filter.setFolioCifCtrl(folioCifCtrl);
		return this;
	}

	public CCReclasificacionFilterBuild setTipoMov(Integer tipoMov) {
		filter.setTipoMov(tipoMov);
		return this;
	}
}
