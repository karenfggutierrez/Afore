package mx.secure.nci.business.wrapped;

import java.util.List;

public class MonitorDomiTraspasoFilter{
	private String cveSol;
	private String cuentaInd;
	private String nss;
	private String curp;
	private List<Short> origenSol;
	private Short idOrigenSol;
	
	public MonitorDomiTraspasoFilter(){	
	}
	
	public MonitorDomiTraspasoFilter(String cuentaInd,String nss,String curp){
		this.cuentaInd = cuentaInd;
		this.nss = nss;
		this.curp = curp;
	}
	
	public String getCveSol() {
		return cveSol;
	}
	
	public void setCveSol(String cveSol) {
		this.cveSol = cveSol;
	}
	
	public String getCuentaInd() {
		return cuentaInd;
	}
	
	public void setCuentaInd(String cuentaInd) {
		this.cuentaInd = cuentaInd;
	}
	
	public String getNss() {
		return nss;
	}
	
	public void setNss(String nss) {
		this.nss = nss;
	}
	
	public String getCurp() {
		return curp;
	}
	
	public void setCurp(String curp) {
		this.curp = curp;
	}
	
	public List<Short> getOrigenSol() {
		return origenSol;
	}
	
	public void setOrigenSol(List<Short> origenSol) {
		this.origenSol = origenSol;
	}

	public Short getIdOrigenSol() {
		return idOrigenSol;
	}

	public void setIdOrigenSol(Short idOrigenSol) {
		this.idOrigenSol = idOrigenSol;
	}	
}