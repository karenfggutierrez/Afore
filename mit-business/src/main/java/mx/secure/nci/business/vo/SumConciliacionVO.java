package mx.secure.nci.business.vo;

import java.math.BigDecimal;

import mx.secure.nci.business.vo.AbstractAuditoriaVO;
import mx.secure.nci.business.vo.GenericCatalogoVO;

public class SumConciliacionVO extends AbstractAuditoriaVO{
	private String folio;
	private GenericCatalogoVO origen;
	private BigDecimal importe;
	private GenericCatalogoVO empresa;
	private Short regConciliado;
	private int rowId;
	private GenericCatalogoVO detalleOrigen; 
	
	public int getRowId() {
		return rowId;
	}
	public void setRowId(int rowId) {
		this.rowId = rowId;
	}
	
	public String getFolio() {
		return folio;
	}
	public void setFolio(String folio) {
		this.folio = folio;
	}
	public GenericCatalogoVO getOrigen() {
		return origen;
	}
	public void setOrigen(GenericCatalogoVO origen) {
		this.origen = origen;
	}
	public BigDecimal getImporte() {
		return importe;
	}
	public void setImporte(BigDecimal importe) {
		this.importe = importe;
	}
	public GenericCatalogoVO getEmpresa() {
		return empresa;
	}
	public void setEmpresa(GenericCatalogoVO empresa) {
		this.empresa = empresa;
	}
	public Short getRegConciliado() {
		return regConciliado;
	}
	public void setRegConciliado(Short regConciliado) {
		this.regConciliado = regConciliado;
	}
	public GenericCatalogoVO getDetalleOrigen() {
		return detalleOrigen;
	}
	public void setDetalleOrigen(GenericCatalogoVO detalleOrigen) {
		this.detalleOrigen = detalleOrigen;
	}

}
