package mx.secure.nci.business.vo.cif;

import java.util.Date;

public class DevolucionesEnvioCifDetalleVO {
	
	private String folio;
	private String unidadNegocio;
	private String origen;
	private Integer numSecuencia;
	private String tipoAfectacion;
	private String subTipoAfectacion;
	private Double monto;
	private Short porcentaje;
	private String itemDevolucion;
	private Date fechaCreacion;
	private String usuCreacion;
	private Date fechaActualizacion;
	private String usuActualizacion;
	private String  ftcSelecc;
	private String usoAutorizo;
	
	public DevolucionesEnvioCifDetalleVO() {
		super();
	}
	
	public DevolucionesEnvioCifDetalleVO(String itemDevolucion) {
		super();
		this.itemDevolucion = itemDevolucion;
	}

	public DevolucionesEnvioCifDetalleVO(Date fechaCreacion, Date fechaActualizacion) {
		super();
		this.fechaCreacion = fechaCreacion;
		this.fechaActualizacion = fechaActualizacion;
	}

	
	public String getUsoAutorizo() {
		return usoAutorizo;
	}

	public void setUsoAutorizo(String usoAutorizo) {
		this.usoAutorizo = usoAutorizo;
	}

	public String getFolio() {
		return folio;
	}
	public void setFolio(String folio) {
		this.folio = folio;
	}
	public String getUnidadNegocio() {
		return unidadNegocio;
	}
	public void setUnidadNegocio(String unidadNegocio) {
		this.unidadNegocio = unidadNegocio;
	}
	public String getOrigen() {
		return origen;
	}
	public void setOrigen(String origen) {
		this.origen = origen;
	}
	public Integer getNumSecuencia() {
		return numSecuencia;
	}
	public void setNumSecuencia(Integer numSecuencia) {
		this.numSecuencia = numSecuencia;
	}
	public String getTipoAfectacion() {
		return tipoAfectacion;
	}
	public void setTipoAfectacion(String tipoAfectacion) {
		this.tipoAfectacion = tipoAfectacion;
	}
	public String getSubTipoAfectacion() {
		return subTipoAfectacion;
	}
	public void setSubTipoAfectacion(String subTipoAfectacion) {
		this.subTipoAfectacion = subTipoAfectacion;
	}
	public Double getMonto() {
		return monto;
	}
	public void setMonto(Double monto) {
		this.monto = monto;
	}
	public Short getPorcentaje() {
		return porcentaje;
	}
	public void setPorcentaje(Short porcentaje) {
		this.porcentaje = porcentaje;
	}
	public String getItemDevolucion() {
		return itemDevolucion;
	}
	public void setItemDevolucion(String itemDevolucion) {
		this.itemDevolucion = itemDevolucion;
	}
	public Date getFechaCreacion() {
		return fechaCreacion;
	}
	public void setFechaCreacion(Date fechaCreacion) {
		this.fechaCreacion = fechaCreacion;
	}
	public String getUsuCreacion() {
		return usuCreacion;
	}
	public void setUsuCreacion(String usuCreacion) {
		this.usuCreacion = usuCreacion;
	}
	public Date getFechaActualizacion() {
		return fechaActualizacion;
	}
	public void setFechaActualizacion(Date fechaActualizacion) {
		this.fechaActualizacion = fechaActualizacion;
	}
	public String getUsuActualizacion() {
		return usuActualizacion;
	}
	public void setUsuActualizacion(String usuActualizacion) {
		this.usuActualizacion = usuActualizacion;
	}
	public String getFtcSelecc() {
		return ftcSelecc;
	}

	public void setFtcSelecc(String ftcSelecc) {
		this.ftcSelecc = ftcSelecc;
	}
	
	
}
