package mx.secure.nci.business.vo;

import java.math.BigDecimal;

public class SolicitudSumTotalPorBancoVO {
	
	private Long numRegistros;
	private BigDecimal total;
	private GenericCatalogoVO tipoCuenta;
	private GenericCatalogoVO claveBanco;
	private GenericCatalogoVO tipoArchivo;
	
	
	public Long getNumRegistros() {
		return numRegistros;
	}
	public void setNumRegistros(Long numRegistros) {
		this.numRegistros = numRegistros;
	}
	public BigDecimal getTotal() {
		return total;
	}
	public void setTotal(BigDecimal total) {
		this.total = total;
	}
	public GenericCatalogoVO getTipoCuenta() {
		return tipoCuenta;
	}
	public void setTipoCuenta(GenericCatalogoVO tipoCuenta) {
		this.tipoCuenta = tipoCuenta;
	}
	public GenericCatalogoVO getClaveBanco() {
		return claveBanco;
	}
	public void setClaveBanco(GenericCatalogoVO claveBanco) {
		this.claveBanco = claveBanco;
	}
	public GenericCatalogoVO getTipoArchivo() {
		return tipoArchivo;
	}
	public void setTipoArchivo(GenericCatalogoVO tipoArchivo) {
		this.tipoArchivo = tipoArchivo;
	}
	
	
}
