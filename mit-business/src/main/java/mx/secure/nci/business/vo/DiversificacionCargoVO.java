package mx.secure.nci.business.vo;

import java.math.BigDecimal;

public class DiversificacionCargoVO extends DiversificacionVO {
	private BigDecimal montoRecibido;
	private Integer idCargo;
	private Integer idArchivo;
	/**
	 * {@value} valor relacionado con la tabla TCCRXGRAL_CAT_PRIORIDAD mediante el idFondo que debe estar configurado en dicha tabla
	 */
	private Short prioridad;

	
	public BigDecimal getMontoRecibido() {
		return montoRecibido;
	}
	public void setMontoRecibido(BigDecimal montoRecibido) {
		this.montoRecibido = montoRecibido;
	}
	public Short getPrioridad() {
		return prioridad;
	}
	public void setPrioridad(Short prioridad) {
		this.prioridad = prioridad;
	}
	public Integer getIdCargo() {
		return idCargo;
	}
	public void setIdCargo(Integer idCargo) {
		this.idCargo = idCargo;
	}
	public Integer getIdArchivo() {
		return idArchivo;
	}
	public void setIdArchivo(Integer idArchivo) {
		this.idArchivo = idArchivo;
	}
	
}
